class Aurora {
    constructor(container) {
        this.container = container;
        this.loadOgl().then(() => {
            this.init();
        }).catch(err => console.error(err));
    }

    loadOgl() {
        return new Promise((resolve, reject) => {
            if (window.ogl) {
                return resolve();
            }
            const script = document.createElement('script');
            script.src = 'https://cdn.jsdelivr.net/npm/ogl/dist/ogl.umd.js';
            script.onload = () => {
                if (window.ogl) {
                    resolve();
                } else {
                    reject('ogl not found on window object');
                }
            };
            script.onerror = reject;
            document.head.appendChild(script);
        });
    }

    init() {
        const VERT = `#version 300 es
            in vec2 position;
            void main() {
              gl_Position = vec4(position, 0.0, 1.0);
            }
        `;

        const FRAG = `#version 300 es
            precision highp float;

            uniform float uTime;
            uniform float uAmplitude;
            uniform vec3 uColorStops[3];
            uniform vec2 uResolution;
            uniform float uBlend;

            out vec4 fragColor;

            vec3 permute(vec3 x) {
              return mod(((x * 34.0) + 1.0) * x, 289.0);
            }

            float snoise(vec2 v){
              const vec4 C = vec4(
                  0.211324865405187, 0.366025403784439,
                  -0.577350269189626, 0.024390243902439
              );
              vec2 i  = floor(v + dot(v, C.yy));
              vec2 x0 = v - i + dot(i, C.xx);
              vec2 i1 = (x0.x > x0.y) ? vec2(1.0, 0.0) : vec2(0.0, 1.0);
              vec4 x12 = x0.xyxy + C.xxzz;
              x12.xy -= i1;
              i = mod(i, 289.0);

              vec3 p = permute(
                  permute(i.y + vec3(0.0, i1.y, 1.0))
                + i.x + vec3(0.0, i1.x, 1.0)
              );

              vec3 m = max(
                  0.5 - vec3(
                      dot(x0, x0),
                      dot(x12.xy, x12.xy),
                      dot(x12.zw, x12.zw)
                  ), 
                  0.0
              );
              m = m * m;
              m = m * m;

              vec3 x = 2.0 * fract(p * C.www) - 1.0;
              vec3 h = abs(x) - 0.5;
              vec3 ox = floor(x + 0.5);
              vec3 a0 = x - ox;
              m *= 1.79284291400159 - 0.85373472095314 * (a0*a0 + h*h);

              vec3 g;
              g.x  = a0.x  * x0.x  + h.x  * x0.y;
              g.yz = a0.yz * x12.xz + h.yz * x12.yw;
              return 130.0 * dot(m, g);
            }

            struct ColorStop {
              vec3 color;
              float position;
            };

            void main() {
              vec2 uv = gl_FragCoord.xy / uResolution;
              
              ColorStop colors[3];
              colors[0] = ColorStop(uColorStops[0], 0.0);
              colors[1] = ColorStop(uColorStops[1], 0.5);
              colors[2] = ColorStop(uColorStops[2], 1.0);
              
              vec3 rampColor;
              
              int index = 0;
              for (int i = 0; i < 2; i++) {
                 ColorStop currentColor = colors[i];
                 bool isInBetween = currentColor.position <= uv.x;
                 index = int(mix(float(index), float(i), float(isInBetween)));
              }
              ColorStop currentColor = colors[index];
              ColorStop nextColor = colors[index + 1];
              float range = nextColor.position - currentColor.position;
              float lerpFactor = (uv.x - currentColor.position) / range;
              rampColor = mix(currentColor.color, nextColor.color, lerpFactor);
              
              float height = snoise(vec2(uv.x * 2.0 + uTime * 0.1, uTime * 0.25)) * 0.5 * uAmplitude;
              height = exp(height);
              height = (uv.y * 2.0 - height + 0.2);
              float intensity = 0.6 * height;
              
              float midPoint = 0.20;
              float auroraAlpha = smoothstep(midPoint - uBlend * 0.5, midPoint + uBlend * 0.5, intensity);
              
              vec3 auroraColor = intensity * rampColor;
              
              fragColor = vec4(auroraColor * auroraAlpha, auroraAlpha);
            }
        `;

        const renderer = new ogl.Renderer({
            alpha: true,
            premultipliedAlpha: true,
            antialias: true,
            version: 2
        });
        const gl = renderer.gl;
        this.container.appendChild(gl.canvas);
        gl.clearColor(0, 0, 0, 0);
        gl.enable(gl.BLEND);
        gl.blendFunc(gl.ONE, gl.ONE_MINUS_SRC_ALPHA);

        let program;

        const resize = () => {
            const width = this.container.offsetWidth;
            const height = this.container.offsetHeight;
            renderer.setSize(width, height);
            if (program) {
                program.uniforms.uResolution.value = [width, height];
            }
        }
        window.addEventListener('resize', resize, false);

        const geometry = new ogl.Triangle(gl);

        const lightColors = ['#ff0099', '#ff66b2', '#ff33a6'].map(hex => {
            const c = new ogl.Color(hex);
            return [c.r, c.g, c.b];
        });
        const darkColors = ['#ff0099', '#ff66b2', '#ff99cc'].map(hex => {
            const c = new ogl.Color(hex);
            return [c.r, c.g, c.b];
        });

        const theme = document.documentElement.getAttribute('data-theme');
        const initialColorStops = theme === 'light' ? lightColors : darkColors;
        const initialAmplitude = theme === 'light' ? 0.5 : 1.0;
        const initialBlend = theme === 'light' ? 0.3 : 0.5;

        program = new ogl.Program(gl, {
            vertex: VERT,
            fragment: FRAG,
            uniforms: {
                uTime: { value: 0 },
                uAmplitude: { value: initialAmplitude },
                uColorStops: { value: initialColorStops },
                uResolution: { value: [this.container.offsetWidth, this.container.offsetHeight] },
                uBlend: { value: initialBlend }
            }
        });

        const mesh = new ogl.Mesh(gl, { geometry, program });

        const update = (t) => {
            requestAnimationFrame(update);
            program.uniforms.uTime.value = t * 0.0005;
            
            const currentTheme = document.documentElement.getAttribute('data-theme');
            if (currentTheme === 'light') {
                program.uniforms.uAmplitude.value = 0.5;
                program.uniforms.uBlend.value = 0.3;
                program.uniforms.uColorStops.value = lightColors;
            } else {
                program.uniforms.uAmplitude.value = 1.0;
                program.uniforms.uBlend.value = 0.5;
                program.uniforms.uColorStops.value = darkColors;
            }

            renderer.render({ scene: mesh });
        }
        requestAnimationFrame(update);

        resize();
    }
}
